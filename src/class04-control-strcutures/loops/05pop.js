/*
     Removes the last element from an array and returns it.
     If the array is empty, undefined is returned and the array is not modified.
 */
const people = ["Janis Joplin", "Jimi Hendrix", "Robert Jhonson", "Ozzy Osbourne"]

people.pop()

console.log(people) // > [ 'Janis Joplin', 'Jimi Hendrix', 'Robert Jhonson' ]
