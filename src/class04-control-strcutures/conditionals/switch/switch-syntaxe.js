const dayWeek = "Quinta"


switch (dayWeek) {
    case "Segunda":
        console.log("Hoje é um péssimo dia")
        break;
    case "Terça":
        console.log("Hojé é o 2º pior dia")
        break;
    case "Quarta":
        console.log("Hoje é dia de jogo na TV")
        break;
    case "Quinta":
        console.log("Falta só 1 dia")
        break;
    case "Sexta":
        console.log("Dia de tomar uma")
        break;
    case "Sabádo":
        console.log("Hoje é dia de tomar umas dnv !!! ")
        break;
    case "Domingo":
        console.log("Hoje é dia de Faustão")
        break;
    default:
        console.log("Dia inválido")
}
